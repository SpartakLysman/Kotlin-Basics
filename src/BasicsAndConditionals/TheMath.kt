package BasicsAndConditionals

import kotlin.math.max
import kotlin.math.min
import kotlin.math.sqrt

fun main() {
    val number1 = 10
    val number2 = 3
    println(kotlin.math.PI)

    println(max(number1, number2))
    println(min(number1, number2))
    println(sqrt(5.0))
    println(kotlin.math.round(4.5))
}